            <p>La fermentation qui s’opère est dite lactique puisque les bactéries créent de l’acide lactique (yahourt, fromage) tandis que les levures  créent de l’alcool et du CO2 (responsable des alvéoles du pain après cuisson).  Plusieurs effets sont observables :</p>

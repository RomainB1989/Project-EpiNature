                <li>L’activation d’enzymes contenus naturellement dans la farine comme :
                    <ul>
                        <li>La protéase : son rôle est de couper les protéines, notamment  les gliadines et gluténines responsables du réseau de gluten. Nous  devons la texture de nos pains ainsi que leur digestibilité au travail de cette enzyme.</li>
                        <li>La phytase : elle dégrade l’acide phytique se qui rend les minéraux plus facilement assimilables.</li>
                    </ul>
